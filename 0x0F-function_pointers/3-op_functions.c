#include "3-calc.h"
/**
 * op_add - adds
 * @a: Holds number 1
 * @b: Holds number 2
 * Return: the result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substracts
 * @a: Holds number 1
 * @b: Holds number 2
 * Return: the result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Multiplies
 * @a: Holds number 1
 * @b: Holds number 2
 * Return: the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides
 * @a: Holds number 1
 * @b: Holds number 2
 * Return: the result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modules
 * @a: Holds number 1
 * @b: Holds number 2
 * Return: the result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
